x = (3, 10, 12, 34, 20, 5, 100, 2022, 18, 189)

# pobieramy elementy
print(x[1])  # drugi element
print(x[-2])  # przedostatni
print(x[3:8])  # od 3 do 7(włącznie)
print(x[::3])  # co trzeci element zaczynajac od 0
print(x[3::3])  # co trzeci element ele bez pierwszego, a odrazu od 3
print(x[::-2])  # co drugi element liczac od konca
print(x[-3::-2])  # co drugi element liczac od konca ale bez pierwszego(z konca)
